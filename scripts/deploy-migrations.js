/**
 * Script para criar a tabela de notificações no Supabase
 * Uso: node scripts/deploy-migrations.js
 */

const { createClient } = require('@supabase/supabase-js');
const fs = require('fs');
const path = require('path');

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !supabaseServiceKey) {
    console.error('❌ Variáveis de ambiente SUPABASE_URL e SUPABASE_SERVICE_ROLE_KEY são obrigatórias');
    process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseServiceKey, {
    auth: { persistSession: false }
});

async function executeMigration(sql) {
    const { data, error } = await supabase.rpc('exec', { query: sql });
    if (error) {
        console.error('❌ Erro ao executar SQL:', error.message);
        return false;
    }
    return true;
}

async function createNotificationsTable() {
    console.log('📦 Criando tabela notifications...');

    const sql = `
    -- ============================================
    -- Tabela de Notificações
    -- ============================================
    CREATE TABLE IF NOT EXISTS public.notifications (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        user_id UUID REFERENCES auth.users(id) NOT NULL,
        titulo TEXT NOT NULL,
        mensagem TEXT NOT NULL,
        tipo TEXT NOT NULL CHECK (tipo IN (
            'parcela_vencendo',
            'parcela_vencida',
            'tarefa_pendente',
            'aniversario',
            'followup',
            'sistema'
        )),
        referencia_id BIGINT,
        referencia_tipo TEXT,
        lida BOOLEAN DEFAULT false,
        created_at TIMESTAMPTZ DEFAULT NOW()
    );

    -- RLS
    ALTER TABLE public.notifications ENABLE ROW LEVEL SECURITY;

    DROP POLICY IF EXISTS "users_own_notifications_select" ON public.notifications;
    DROP POLICY IF EXISTS "users_own_notifications_insert" ON public.notifications;
    DROP POLICY IF EXISTS "users_own_notifications_delete" ON public.notifications;

    CREATE POLICY "users_own_notifications_select" ON public.notifications FOR SELECT USING (auth.uid() = user_id);
    CREATE POLICY "users_own_notifications_insert" ON public.notifications FOR INSERT WITH CHECK (auth.uid() = user_id);
    CREATE POLICY "users_own_notifications_delete" ON public.notifications FOR DELETE USING (auth.uid() = user_id);

    -- Índices
    CREATE INDEX IF NOT EXISTS idx_notifications_user_id ON public.notifications(user_id);
    CREATE INDEX IF NOT EXISTS idx_notifications_lida ON public.notifications(user_id, lida);
    CREATE INDEX IF NOT EXISTS idx_notifications_created_at ON public.notifications(created_at DESC);

    -- Permissões para service role
    GRANT ALL ON public.notifications TO service_role;

    -- Função para criar notificação (chamada pelas edge functions)
    CREATE OR REPLACE FUNCTION public.criar_notificacao(
        p_user_id UUID,
        p_titulo TEXT,
        p_mensagem TEXT,
        p_tipo TEXT,
        p_referencia_id BIGINT DEFAULT NULL,
        p_referencia_tipo TEXT DEFAULT NULL
    )
    RETURNS void
    LANGUAGE plpgsql
    SECURITY DEFINER
    AS $$
    BEGIN
        INSERT INTO public.notifications (user_id, titulo, mensagem, tipo, referencia_id, referencia_tipo)
        VALUES (p_user_id, p_titulo, p_mensagem, p_tipo, p_referencia_id, p_referencia_tipo);
    END;
    $$;

    GRANT EXECUTE ON FUNCTION public.criar_notificacao TO service_role;
    `;

    const { error } = await supabase.query(sql);
    if (error) {
        console.error('❌ Erro ao criar tabela:', error.message);
        return false;
    }
    console.log('✅ Tabela notifications criada com sucesso!');
    return true;
}

async function setupCronJobs() {
    console.log('⏰ Configurando cron jobs...');

    const serviceKey = supabaseServiceKey;
    const projectRef = supabaseUrl.replace('https://', '').replace('.supabase.co', '');

    const cronJobs = [
        { name: 'check-parcelas-cron', schedule: '0 * * * *', url: `https://${projectRef}.supabase.co/functions/v1/check-parcelas` },
        { name: 'check-tarefas-cron', schedule: '0 * * * *', url: `https://${projectRef}.supabase.co/functions/v1/check-tarefas` },
        { name: 'check-aniversarios-cron', schedule: '0 9 * * *', url: `https://${projectRef}.supabase.co/functions/v1/check-aniversarios` },
        { name: 'check-followup-cron', schedule: '0 10 * * *', url: `https://${projectRef}.supabase.co/functions/v1/check-followup` }
    ];

    for (const job of cronJobs) {
        const sql = `
        SELECT cron.schedule(
            '${job.name}',
            '${job.schedule}',
            $$
            SELECT net.http_post(
                url:='${job.url}',
                headers:='{"Content-Type": "application/json", "Authorization": "Bearer ${serviceKey}"}'::jsonb
            ) AS result;
            $$
        );
        `;
        const { error } = await supabase.query(sql);
        if (error) {
            if (error.message.includes('already exists')) {
                console.log(`  ⏭️  ${job.name} já existe`);
            } else {
                console.log(`  ⚠️  ${job.name}: ${error.message}`);
            }
        } else {
            console.log(`  ✅ ${job.name} criado`);
        }
    }
    return true;
}

async function main() {
    console.log('🚀 Iniciando setup de migrations...\n');

    try {
        const result1 = await createNotificationsTable();
        if (!result1) process.exit(1);

        console.log('');
        const result2 = await setupCronJobs();

        console.log('\n✅ Setup completo!');
        console.log('\n📋 Próximos passos:');
        console.log('1. Deploy as Edge Functions no Supabase Dashboard');
        console.log('2. Acesse: https://supabase.com/dashboard/project/zgtakbznmuxkibxybdky/functions');
        console.log('3. Crie cada função com o código em supabase/functions/');
    } catch (err) {
        console.error('❌ Erro fatal:', err.message);
        process.exit(1);
    }
}

main();