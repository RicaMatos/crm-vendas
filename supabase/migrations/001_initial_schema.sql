-- Função para inserir dados iniciais
CREATE OR REPLACE FUNCTION public.insert_initial_data()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    -- Inserir culturas padrão se não existirem
    INSERT INTO public.crops (user_id, nome, observacoes, cor)
    SELECT auth.uid(), 'Batata', 'Cultivo principal da região serrana', '#2563eb'
    WHERE NOT EXISTS (SELECT 1 FROM public.crops WHERE nome = 'Batata' AND user_id = auth.uid());
    
    INSERT INTO public.crops (user_id, nome, observacoes, cor)
    SELECT auth.uid(), 'Soja', 'Grão para exportação', '#16a34a'
    WHERE NOT EXISTS (SELECT 1 FROM public.crops WHERE nome = 'Soja' AND user_id = auth.uid());
    
    INSERT INTO public.crops (user_id, nome, observacoes, cor)
    SELECT auth.uid(), 'Café', 'Variedade arábica', '#ea580c'
    WHERE NOT EXISTS (SELECT 1 FROM public.crops WHERE nome = 'Café' AND user_id = auth.uid());
END;
$$;

