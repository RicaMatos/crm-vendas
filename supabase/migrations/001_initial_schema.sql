-- Função para atualizar updated_at automaticamente
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Triggers para atualizar updated_at
CREATE TRIGGER update_customers_updated_at BEFORE UPDATE ON public.customers
    FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_products_updated_at BEFORE UPDATE ON public.products
    FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_orders_updated_at BEFORE UPDATE ON public.orders
    FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_tasks_updated_at BEFORE UPDATE ON public.tasks
    FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- ============================================
-- FUNÇÃO PARA GERAR NÚMERO DE PEDIDO
-- ============================================

CREATE OR REPLACE FUNCTION public.generate_order_number()
RETURNS TEXT AS $$
DECLARE
    prefixo TEXT := 'PED';
    ano TEXT;
    mes TEXT;
    dia TEXT;
    sequencial TEXT;
    numero TEXT;
BEGIN
    ano := TO_CHAR(NOW(), 'YY');
    mes := TO_CHAR(NOW(), 'MM');
    dia := TO_CHAR(NOW(), 'DD');
    
    -- Gera um número aleatório de 4 dígitos
    sequencial := LPAD(FLOOR(RANDOM() * 10000)::TEXT, 4, '0');
    
    numero := prefixo || ano || mes || dia || sequencial;
    
    -- Verifica se já existe, se existir gera novamente
    IF EXISTS (SELECT 1 FROM public.orders WHERE numero_pedido = numero) THEN
        sequencial := LPAD(FLOOR(RANDOM() * 10000)::TEXT, 4, '0');
        numero := prefixo || ano || mes || dia || sequencial;
    END IF;
    
    RETURN numero;
END;
$$ LANGUAGE plpgsql;

